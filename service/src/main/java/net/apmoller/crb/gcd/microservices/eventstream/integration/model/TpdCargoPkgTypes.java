package net.apmoller.crb.gcd.microservices.eventstream.integration.model;

public class TpdCargoPkgTypes {

	private String instanceId;

	private String createdDate;

	private String cargoTypeDescription;

	private String cargoPackageTypeId;

	private String updatedDate;

	private String cargoTypeCode;

	public String getInstanceId() {
		return instanceId;
	}

	public void setInstanceId(String instanceId) {
		this.instanceId = instanceId;
	}

	public String getCreatedDate() {
		return createdDate;
	}

	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}

	public String getCargoTypeDescription() {
		return cargoTypeDescription;
	}

	public void setCargoTypeDescription(String cargoTypeDescription) {
		this.cargoTypeDescription = cargoTypeDescription;
	}

	public String getCargoPackageTypeId() {
		return cargoPackageTypeId;
	}

	public void setCargoPackageTypeId(String cargoPackageTypeId) {
		this.cargoPackageTypeId = cargoPackageTypeId;
	}

	public String getUpdatedDate() {
		return updatedDate;
	}

	public void setUpdatedDate(String updatedDate) {
		this.updatedDate = updatedDate;
	}

	public String getCargoTypeCode() {
		return cargoTypeCode;
	}

	public void setCargoTypeCode(String cargoTypeCode) {
		this.cargoTypeCode = cargoTypeCode;
	}

}
